int * Wheel(int WheelPos) {
  WheelPos = 255 - WheelPos;
  static int color[3] = {0};
  if(WheelPos < 85) {
    color[0] = 255 - WheelPos * 3;
	color[1] = 0;
	color[2] = WheelPos * 3;
	return color;
  }
  if(WheelPos < 170) {
    WheelPos -= 85;
   color[0] = 0;
   color[1] = WheelPos * 3;
	color[2] = 255 - WheelPos * 3;
   return color;
  }
  WheelPos -= 170;
  color[0] = WheelPos * 3;
  color[1] = 255 - WheelPos * 3;
  color[2] = 0;
  return color;
}

